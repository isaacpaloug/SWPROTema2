package cat.paucasesnoves.isaac;

public class Ejercici1 {
	/** Exercici 1

	* Calcula la longitud d'una circumferència a partir del radi.

	* @author Isaac

	*/
	public static void main(String[] args) {
		double pi = Math.PI;
		double radio = 45;
		double longitud;
		longitud = 2 * pi * radio;
		System.out.println( "Longitud: " + longitud);
	}
}
