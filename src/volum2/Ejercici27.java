package volum2;

public class Ejercici27 {

	public static void main(String[] args) {
		for (char c = 'a'; c <= 'z'; c++) {
			if (c == 'a') {
				System.out.println(c + " és vocal");
			} else if (c == 'e') {
				System.out.println(c + " és vocal");
			} else if (c == 'i') {
				System.out.println(c + " és vocal");
			} else if (c == 'o') {
				System.out.println(c + " és vocal");
			} else if (c == 'u') {
				System.out.println(c + " és vocal");
			} else {
				System.out.println(c + " és consonant");
			}
		}
	}

}
